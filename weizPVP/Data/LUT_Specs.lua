-- ⬅️ Get Role from Spec
---- @return {String:"DAMAGER" | "TANK" | "HEALER" | nil}
local function GetRoleFromSpecId(specId)
    if specId then
        return select(5, GetSpecializationInfoByID(specId))
    end
    return nil
end

-- ⬅️ GET SPEC FROM SpellID
local function GetSpecIdFromSpellId(spellId)
    return LUT[spellId] or nil
end

-- ⬅️ GET ROLE FROM SpellID
----------------------------------------------------------------
function NS.GetRoleFromSpellId(spellId)
    return GetRoleFromSpecId(GetSpecIdFromSpellId(spellId))
end
